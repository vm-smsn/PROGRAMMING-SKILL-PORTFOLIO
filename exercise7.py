# For this exercise I have utilized the "for" loop which allows the code to 
# execute the code in sequence. It checks if the current elements is within sequence 
# and if it is then and only then, will it continue to the next "for" loop.

# This command counts up from 0 to 50 in increments of 1.
for taho in range(51):
    print(taho)

# This command counts down from 50 to 0 in decrements of 1.
# Range is (start, stop, step) thus explaining the numbers withing the command.
for ubepandesal in range(50, -1, -1):
    print(ubepandesal)

# This command counts up from 30 to 50 in increments of 1.
# As you can see there is no third number because by default â‡£
# the number increases by 1.
for icescramble in range(30, 51):
    print(icescramble)

# This command counts down from 50 to 10 in decrements of 2.
for champorado in range(50, 9, -2):
    print(champorado)

# This command counts up from 100 to 200 in increments of 5
for pilipinsss in range(100, 201, 5):
    print(pilipinsss)